¿Cuál es el significado de la presentación de niño para el día de hoy?
La presentación de bebe o niños es una práctica que encontramos en la Biblia, aunque no es un mandato para
los cristianos el día de hoy, pero creemos que es una ceremonia significativa, ya que refleja el compromiso de
los padres de criar a sus niños bajo la guía del Dios y de enseñarles con el ejemplo.
“Instruye al niño en el camino que debe seguir; aun cuando fuere viejo no se apartará de él”. Proverbios 22:6
La presentación de niños tiene más que ver con los padres y su relación con Dios, que con los niños que se
presentan. Cuando los padres desean que sus niños sean presentados al Señor, esto indica que ellos
reconocen a Dios como el dador de la vida y que también están pidiendo al Señor que bendiga y proteja a su
niño(s), confiando que el Señor lo hará.
Presentación de niños y el bautismo.
La palabra “bautizar” es un extranjerismo del idioma griego incorporado a nuestro idioma. Significa
simplemente: sumergir, hundir en agua o sepultar.
Esta palabra se encuentra unas ochenta veces a lo largo del Nuevo Testamento y es una ordenanza que tiene
su origen en el mandamiento que recibieron del Señor los primeros creyentes.
“Jesús se acercó entonces a ellos y les dijo: —Se me ha dado toda autoridad en el cielo y en la tierra. Por tanto,
vayan y hagan discípulos de todas las naciones, bautizándolos en el nombre del Padre y del Hijo y del Espíritu
Santo, enseñándoles a obedecer todo lo que les he mandado a ustedes. Y les aseguro que estaré con ustedes
siempre, hasta el fin del mundo”. Mateo 28:18 – 20 NVI
Al bautizarnos damos un testimonio público de nuestra fe depositada en el Señor Jesucristo. Nos declaramos
muertos para el mundo, pero vivos para agradar y obedecer a Dios.
También debemos destacar que en el Nuevo testamento no encontramos ningún ejemplo del bautismo de
bebés, pero sí de personas que entendieron el mensaje del evangelio y confiaron en Jesucristo como Salvador,
para después ser bautizadas.
Así, pues, los que recibieron su mensaje fueron bautizados, y aquel día se unieron a la iglesia unas tres mil
personas. Hechos 2:41 NVI
Ahora, conociendo que la presentación de niños, tiene más que ver con los padres y no es equivalente al
bautismo de los adultos ¿qué espera Dios de nosotros?
Dios desea y nos ayudará en la hermosa tarea de criar a nuestros hijos en las verdades de su Palabra, que un
día conozcan a Jesús como su Salvador y que desarrollen sus vidas, con sus habilidades y talentos en una
actitud de adoración.
“Llevaron unos niños a Jesús para que les impusiera las manos y orara por ellos, pero los discípulos reprendían
a quienes los llevaban. Jesús dijo: «Dejen que los niños vengan a mí, y no se lo impidan, porque el reino de los
cielos es de quienes son como ellos.» Después de poner las manos sobre ellos, se fue de allí”. Mateo 19:13- 15
